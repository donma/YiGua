function buildEntries(hexId, contentMap) {
  const entries = [];
  for (const category of Object.keys(CATEGORY_NAMES)) {
    const c = contentMap[category];
    const idStr = `hex-${String(hexId).padStart(3, '0')}-${category}`;
    entries.push({
      id: idStr,
      hexagramId: hexId,
      category,
      categoryName: CATEGORY_NAMES[category],
      meaning: c.meaning,
      advice: c.advice,
      warning: c.warning,
      timing: c.timing,
      basis: BASIS[hexId][category],
      scoreAdjust: SCORE_ADJUST[category],
      qualityLevel: 'refined',
      reviewed: false,
      reviewedBy: 'category-gold-review',
      reviewedAt: '2026-07-13',
      needsHumanReview: true,
      version: '1.7.1-gold-cat'
    });
  }
  return entries;
}

