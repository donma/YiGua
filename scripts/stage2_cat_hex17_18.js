function lenStats(entries, field) {
  const lens = entries.map(e => e[field].length);
  return {
    min: Math.min(...lens),
    max: Math.max(...lens),
    avg: Math.round(lens.reduce((a, b) => a + b, 0) / lens.length)
  };
}

