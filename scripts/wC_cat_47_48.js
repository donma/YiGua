// ============================================================
const PROHIBITED_PATTERNS = [
  /^\d+[\.\、\)]/m,      // numbered list at start of line
  /第[一二三四五六七八九十]/g,  // ordinal templates
  /首先.*其次.*最後/g,    // template skeleton
  /第一.*第二.*第三/g,    // numbered list template
];

for (const entry of allNewEntries) {
  for (const field of ['meaning', 'advice', 'warning', 'timing']) {
    for (const pattern of PROHIBITED_PATTERNS) {
      if (pattern.test(entry[field])) {
        console.error(`FAIL: ${entry.id} field "${field}" contains prohibited pattern: ${pattern}`);
        allPass = false;
      }
    }
  }
}

if (!allPass) {
  console.error('\nProhibited pattern check failed. Aborting write.');
  process.exit(1);
}

console.log('All prohibited pattern checks passed.\n');

// ============================================================
// LOAD EXISTING DATA
// ============================================================
const rawSource = fs.readFileSync(DATA_PATH, 'utf8');
const sandbox = { window: {} };
vm.createContext(sandbox);
vm.runInContext(rawSource, sandbox);
const existingData = sandbox.window.Zero1MatrixData.categoryInterpretations;

if (!Array.isArray(existingData)) {
  throw new Error('Failed to load existing categoryInterpretations array');
}

// ============================================================
// MERGE: replace hex 47 & 48 entries, keep everything else
// ============================================================
const filteredExisting = existingData.filter(
  (e) => e.hexagramId !== 47 && e.hexagramId !== 48
);

const mergedData = [...filteredExisting, ...allNewEntries].sort((a, b) => {
  if (a.hexagramId !== b.hexagramId) return a.hexagramId - b.hexagramId;
  const order = Object.keys(CATEGORY_NAMES);
  return order.indexOf(a.category) - order.indexOf(b.category);
});

// ============================================================
// WRITE FILE
// ============================================================
const header = "window.Zero1MatrixData = window.Zero1MatrixData || {};\nwindow.Zero1MatrixData.categoryInterpretations = ";
const footer = ";\n";

const jsonBody = JSON.stringify(mergedData, null, 2);
const outputContent = header + jsonBody + footer;

fs.writeFileSync(DATA_PATH, outputContent, 'utf8');
console.log(`Wrote ${mergedData.length} total entries (${allNewEntries.length} new/updated for hex 47 & 48) to:`);
console.log(DATA_PATH);

// ============================================================
// VERIFY with node --check
// ============================================================
const { execSync } = require('child_process');
try {
  execSync(`"${NODE_EXE}" --check "${DATA_PATH}"`, { stdio: 'pipe' });
  console.log('node --check PASSED');
} catch (e) {
  console.error('node --check FAILED');
  console.error(e.stdout ? e.stdout.toString() : '');
  console.error(e.stderr ? e.stderr.toString() : '');
  process.exit(1);
}