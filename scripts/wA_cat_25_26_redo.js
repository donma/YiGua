function assertLength(label, id, field, value, minLen) {
  const actual = value.length;
  if (actual < minLen) {
    throw new Error(
      `FAIL ${id} ${field}: length=${actual} < min=${minLen} | "${value}"`
    );
  }
  return actual;
}

function checkUniqueness(entries, field) {
  const phrases = new Set();
  for (const e of entries) {
    // Split into 5-word sliding windows
    const words = e[field].split('');
    for (let i = 0; i <= words.length - 5; i++) {
      const phrase = words.slice(i, i + 5).join('');
      if (phrases.has(phrase)) {
        console.warn(`  WARN: duplicate 5-char phrase "${phrase}" in ${e.id} ${field}`);
      }
      phrases.add(phrase);
    }
  }
}

function checkBasisLength(id, basis) {
  if (basis.length < 4) {
    throw new Error(`FAIL ${id} basis.length=${basis.length} < 4`);
  }
}

