const LENGTH_CHECKS = {
  meaning:  { min: 120, max: 190, label: 'meaning' },
  advice:   { min: 100, max: 160, label: 'advice' },
  warning:  { min: 80,  max: 125, label: 'warning' },
  timing:   { min: 60,  max: 105, label: 'timing' },
};

function verifyAll(prefix, contentMap) {
  const categories = Object.keys(contentMap);
  let allPass = true;
  let failCount = 0;
  for (const cat of categories) {
    const entry = contentMap[cat];
    if (!entry.basis || entry.basis.length < 4) {
      console.error(`  FAIL ${prefix} ${cat}: basis.length=${entry.basis ? entry.basis.length : 0} < 4`);
      allPass = false; failCount++;
      continue;
    }
    for (const [key, spec] of Object.entries(LENGTH_CHECKS)) {
      const text = entry[key];
      if (!text) {
        console.error(`  FAIL ${prefix} ${cat}: ${spec.label} missing`);
        allPass = false; failCount++;
        continue;
      }
      const len = text.length;
      if (len < spec.min || len > spec.max) {
        console.error(`  FAIL ${prefix} ${cat}: ${spec.label} len=${len} (${spec.min}-${spec.max})`);
        allPass = false; failCount++;
      }
    }
  }
  if (allPass) console.log(`  ${prefix}: ALL ${categories.length} entries PASS`);
  else console.log(`  ${prefix}: ${failCount} failure(s)`);
  return allPass;
}

const PROHIBITED = ['上卦帶來推力','下卦帶來推力','保持彈性並等待時機','保持彈性等待','先觀察再決定','靈活應對','以柔克剛','剛柔並濟'];

function checkProhibitedAll(prefix, contentMap) {
  let ok = true;
  for (const [cat, e] of Object.entries(contentMap))
    for (const f of ['meaning','advice','warning','timing'])
      for (const b of PROHIBITED)
        if (e[f] && e[f].includes(b)) { console.error(`  PROHIBITED ${prefix} ${cat}.${f}: "${b}"`); ok = false; }
  return ok;
}

function checkNoNumberedLists(prefix, contentMap) {
  let ok = true;
  for (const [cat, e] of Object.entries(contentMap))
    for (const f of ['meaning','advice','warning','timing'])
      if (e[f] && /[0-9]+[、．\.\)]/.test(e[f])) { console.error(`  NUMBERED ${prefix} ${cat}.${f}`); ok = false; }
  return ok;
}

function checkDisclaimers(contentMap) {
  let ok = true;
  const checks = {health:'本欄不做醫療診斷', business:'本欄不做獲利保證', legal:'本欄不做法律結果判定'};
  for (const [cat, ph] of Object.entries(checks))
    if (contentMap[cat] && !contentMap[cat].meaning.includes(ph)) { console.error(`  MISSING disclaimer in ${cat}`); ok = false; }
  const sp = contentMap['spiritual'];
  if (sp) for (const b of ['預言','命運','天命注定','神諭','占卜預測'])
    if (sp.meaning.includes(b)) { console.error(`  PROHIBITED in spiritual: "${b}"`); ok = false; }
  return ok;
}

