function buildContentMap(hexId) {
  return hexId === 21 ? HEX21 : HEX22;
}
function buildImageryMap(hexId) {
  return hexId === 21 ? IMAGERY21 : IMAGERY22;
}

let updatedCount = 0;
const categories = Object.keys(SCORE_ADJUST);

for (const hexId of [21, 22]) {
  const contentMap = buildContentMap(hexId);
  const imageryMap = buildImageryMap(hexId);
  const hexName = HEX_NAME[hexId];

  for (const cat of categories) {
    const entry = data.find(d => d.hexagramId === hexId && d.category === cat);
    if (!entry) {
      throw new Error(`Missing entry for hexagramId=${hexId} category=${cat}`);
    }
    const content = contentMap[cat];
    if (!content) {
      throw new Error(`Missing content definition for hex ${hexId} category ${cat}`);
    }
    entry.meaning = content.meaning;
    entry.advice = content.advice;
    entry.warning = content.warning;
    entry.timing = content.timing;
    entry.basis = [hexName, ...imageryMap[cat]];
    entry.scoreAdjust = { ...SCORE_ADJUST[cat] };
    entry.qualityLevel = 'refined';
    entry.reviewed = false;
    entry.needsHumanReview = true;
    updatedCount++;
  }
}

