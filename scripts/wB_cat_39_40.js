function buildEntries(hexagramId, contentMap, scoreMap) {
  const categories = Object.keys(CATEGORY_NAMES);
  return categories.map((category) => {
    const t = contentMap[category];
    return {
      id: `hex-${String(hexagramId).padStart(3, '0')}-${category}`,
      hexagramId,
      category,
      categoryName: CATEGORY_NAMES[category],
      meaning: t.meaning,
      advice: t.advice,
      warning: t.warning,
      timing: t.timing,
      basis: t.basis,
      scoreAdjust: scoreMap[category],
      qualityLevel: 'refined',
      reviewed: false,
      reviewedBy: 'category-hex39-40',
      reviewedAt: '2026-07-13',
      needsHumanReview: true,
      version: '1.7.2-refined-cat-hex39-40',
    };
  });
}

