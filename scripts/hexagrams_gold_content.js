const fs = require("fs");
const path = require("path");

const rootDir = path.resolve(__dirname, "..");
const hexFile = path.join(rootDir, "src", "data", "hexagrams.data.js");
const today = new Date().toISOString().split("T")[0];

const raw = fs.readFileSync(hexFile, "utf8");
const jsonStart = raw.indexOf("= [") + 2;
const jsonEnd = raw.lastIndexOf("];") + 1;
const hexagrams = JSON.parse(raw.substring(jsonStart, jsonEnd));

// Get line texts for per-hex line references
const linesRaw = fs.readFileSync(path.join(rootDir, "src", "data", "lines.data.js"), "utf8");
const linesStart = linesRaw.indexOf("= [") + 2;
const linesEnd = linesRaw.lastIndexOf("];") + 1;
const lines = JSON.parse(linesRaw.substring(linesStart, linesEnd));
const lineMap = {};
for (const l of lines) {
  const key = `${l.hexagramId}-${l.line}`;
  lineMap[key] = l.text;
}

// Helper to get key line texts for a hexagram
function lineRef(hid, ...positions) {
  return positions.map(p => lineMap[`${hid}-${p}`] || "").filter(Boolean);
}

