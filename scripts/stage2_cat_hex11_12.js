function verifyHex(arr, hexId) {
  const cats = new Set(arr.map(e => e.category));
  const missing = categories.filter(c => !cats.has(c));
  if (missing.length > 0) {
    console.log(`Hex ${hexId} MISSING categories: ${missing.join(", ")}`);
    return false;
  }

  let allPass = true;
  for (const e of arr) {
    const ml = (e.meaning || "").length;
    const al = (e.advice || "").length;
    const wl = (e.warning || "").length;
    const tl = (e.timing || "").length;
    const bl = (e.basis || []).length;
    const status = (ml >= 110 && ml <= 300) && (al >= 100 && al <= 280) &&
                   (wl >= 80 && wl <= 180) && (tl >= 60 && tl <= 170) && bl >= 3;
    if (!status) {
      console.log(`Hex ${hexId} ${e.category}: m=${ml} a=${al} w=${wl} t=${tl} b=${bl} FAIL`);
      allPass = false;
    } else {
      console.log(`Hex ${hexId} ${e.category}: m=${ml} a=${al} w=${wl} t=${tl} b=${bl} PASS`);
    }
  }

  // uniqueness within hex
  function norm(text) {
    return String(text || "").replace(/小往大來|拔茅茹|包荒|用馮河|翩翩|帝乙歸妹|城復于隍|天地交|財成天地|無平不陂|否之匪人|不利君子貞|大往小來|包承|包羞|有命無咎|休否|傾否|天地不交|儉德辟難/g, "")
      .replace(/[，。、；：！？\s\d]/g, "").substring(0, 60);
  }
  const mu = new Set(arr.map(e => norm(e.meaning))).size;
  const au = new Set(arr.map(e => norm(e.advice))).size;
  const wu = new Set(arr.map(e => norm(e.warning))).size;
  const tu = new Set(arr.map(e => norm(e.timing))).size;
  const uniqOk = mu >= 8 && au >= 8 && wu >= 8 && tu >= 7;
  console.log(`Hex ${hexId} uniqueness: mU=${mu}/8 aU=${au}/8 wU=${wu}/8 tU=${tu}/8 ${uniqOk ? "PASS" : "FAIL"}`);
  if (!uniqOk) allPass = false;
  return allPass;
}

