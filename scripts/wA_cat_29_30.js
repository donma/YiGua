function validateLengths(contentMap, hexId) {
  const errors = [];
  for (const [cat, c] of Object.entries(contentMap)) {
    const mLen = c.meaning.length;
    const aLen = c.advice.length;
    const wLen = c.warning.length;
    const tLen = c.timing.length;

    if (mLen < 120 || mLen > 190) errors.push(`hex${hexId}.${cat} meaning: ${mLen} chars (need 120-190)`);
    if (aLen < 100 || aLen > 160) errors.push(`hex${hexId}.${cat} advice: ${aLen} chars (need 100-160)`);
    if (wLen < 80 || wLen > 125) errors.push(`hex${hexId}.${cat} warning: ${wLen} chars (need 80-125)`);
    if (tLen < 60 || tLen > 105) errors.push(`hex${hexId}.${cat} timing: ${tLen} chars (need 60-105)`);
    if (!Array.isArray(c.basis) || c.basis.length < 4) errors.push(`hex${hexId}.${cat} basis: need >=4 items, got ${c.basis ? c.basis.length : 0}`);
  }
  return errors;
}

