function buildEntries(hexagramId, hexData, scores) {
  return Object.keys(hexData).map((cat) => {
    const content = hexData[cat];
    return {
      id: `hex-${String(hexagramId).padStart(3, '0')}-${cat}`,
      hexagramId,
      category: cat,
      categoryName: CATEGORY_NAMES[cat],
      meaning: content.meaning,
      advice: content.advice,
      warning: content.warning,
      timing: content.timing,
      basis: content.basis,
      scoreAdjust: scores[cat],
      qualityLevel: 'refined',
      reviewed: false,
      needsHumanReview: true,
      version: `1.7.3-refined-cat-hex33-34`,
    };
  });
}

