function validateHex(hexData, hexName) {
  let ok = true;
  for (const [cat, qs] of Object.entries(hexData)) {
    for (let i = 0; i < qs.length; i++) {
      const len = qs[i][0].length;
      if (len < 38 || len > 105) {
        console.error(`[LENGTH FAIL] ${hexName} ${cat} Q${i+1}: ${len} chars`);
        ok = false;
      }
      if (!qs[i][0].endsWith("？")) {
        console.error(`[QMARK FAIL] ${hexName} ${cat} Q${i+1}: does not end with ？`);
        ok = false;
      }
    }
  }
  return ok;
}

