function applyUpdates(data, hexagramId, contentMap, scoreMap) {
  let count = 0;
  for (const entry of data) {
    if (entry.hexagramId !== hexagramId) continue;
    const c = contentMap[entry.category];
    if (!c) continue;
    entry.meaning = c.meaning;
    entry.advice = c.advice;
    entry.warning = c.warning;
    entry.timing = c.timing;
    entry.basis = c.basis;
    entry.scoreAdjust = scoreMap[entry.category];
    entry.qualityLevel = 'refined';
    entry.reviewed = false;
    entry.needsHumanReview = true;
    count++;
  }
  return count;
}

